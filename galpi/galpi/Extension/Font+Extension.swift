import SwiftUI

extension Font {
    
    static func jejuMyeongjo(size: CGFloat) -> Font {
        return Font.custom("JejuMyeongjoOTF", size: size)
    }
    
    static func koPubBaTangMedium(size: CGFloat) -> Font {
        return Font.custom("KoPubBatangMedium", size: size)
    }
    
    
}
